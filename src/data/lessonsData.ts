// Type definitions
export type Subject = keyof typeof lessonsData;
export type Grade = 6 | 7 | 8 | 9 | 10 | 11 | 12;

// Helper functions
export const getSubjects = (): Subject[] => Object.keys(lessonsData) as Subject[];

export const getGrades = (): Grade[] => [6, 7, 8, 9, 10, 11, 12];

export const getLessonsForSubjectAndGrade = (subject: Subject, grade: Grade): string[] => {
  return lessonsData[subject]?.[grade] || [];
};

export const getAllSubjectsAndGrades = () => {
  const result: { subject: Subject; grade: Grade; lessons: string[] }[] = [];
  
  for (const subject of getSubjects()) {
    for (const grade of getGrades()) {
      const lessons = getLessonsForSubjectAndGrade(subject, grade);
      if (lessons.length > 0) {
        result.push({ subject, grade, lessons });
      }
    }
  }
  
  return result;
};